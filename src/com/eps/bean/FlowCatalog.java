package com.eps.bean;

public class FlowCatalog {
    private String serialNo;

    private String flowNo;

    private String status;

    private String inputUserID;

    private String inputTime;

    private String defaultPhaseNo;

    private String flowName;

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public String getFlowNo() {
		return flowNo;
	}

	public void setFlowNo(String flowNo) {
		this.flowNo = flowNo;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getInputUserID() {
		return inputUserID;
	}

	public void setInputUserID(String inputUserID) {
		this.inputUserID = inputUserID;
	}

	public String getInputTime() {
		return inputTime;
	}

	public void setInputTime(String inputTime) {
		this.inputTime = inputTime;
	}

	public String getDefaultPhaseNo() {
		return defaultPhaseNo;
	}

	public void setDefaultPhaseNo(String defaultPhaseNo) {
		this.defaultPhaseNo = defaultPhaseNo;
	}

	public String getFlowName() {
		return flowName;
	}

	public void setFlowName(String flowName) {
		this.flowName = flowName;
	}

}
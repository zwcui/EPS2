package com.eps.bean;

public class FlowRelative {
    private String serialNo;

    private String taskSerialNo;

    private String nextTaskSerialNo;

    private String flowSerialNo;

	public String getSerialNo() {
		return serialNo;
	}

	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}

	public String getTaskSerialNo() {
		return taskSerialNo;
	}

	public void setTaskSerialNo(String taskSerialNo) {
		this.taskSerialNo = taskSerialNo;
	}

	public String getNextTaskSerialNo() {
		return nextTaskSerialNo;
	}

	public void setNextTaskSerialNo(String nextTaskSerialNo) {
		this.nextTaskSerialNo = nextTaskSerialNo;
	}

	public String getFlowSerialNo() {
		return flowSerialNo;
	}

	public void setFlowSerialNo(String flowSerialNo) {
		this.flowSerialNo = flowSerialNo;
	}

}